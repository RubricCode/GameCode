from commands.command import MuxCommand


class CmdHuman(MuxCommand):
    """
    speak as the human language

    Usage:
      human <message>
      human to target <message>

    Talk to those in your current location in the human tongue.
    """

    key = "human"
    aliases = ['"', "'"]
    locks = "cmd:all()"

    def parse(self):
        """Implement an addition parse"""
        super(CmdHuman, self).parse()
        if "to" in self.args:
            self.lhs, self.rhs = self.args.split(" to ", 1)

    def func(self):
        """Run the human command"""

        caller = self.caller

        if not self.args:
            caller.msg("Say what?")
            return

        speech = self.args

        # Calling the at_before_say hook on the character
        speech = caller.at_before_say(speech)

        # If speech is empty, stop here
        if not speech:
            return

        # Call the at_after_say hook on the character
        caller.at_say(speech, msg_self=True)


